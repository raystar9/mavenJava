package week1.day3;

public class ComAssign {

	public static void main(String[] args) {
		
		int result = 10, a = 2;
		
		result += a;
		System.out.println(result);
		
		result -= a;
		System.out.println(result);
		
		result *= a;
		System.out.println(result);
		
		result /= a;
		System.out.println(result);
	}
}