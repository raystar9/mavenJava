package week1.day3;

public class Switch {

	public static void main(String[] args) {
		
		int num = 2;
		switch (num) {
		case 0 : 
			System.out.println("Good Morning, JAVA");
			break;
		case 1 : 
			System.out.println("Good Afternoon, JAVA");
			break;
		case 2 : 
			System.out.println("Good Evening, JAVA");
			break;
		default : 
			System.out.println("Good Night, JAVA");
			break;
		}
	}
}
