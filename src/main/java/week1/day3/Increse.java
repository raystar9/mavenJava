package week1.day3;

public class Increse {

	public static void main(String[] args) {
		
		int i = 0;
		
		i++;
		System.out.println(i);
		
		++i;
		System.out.println(i);
		
		i--;
		System.out.println(i);
		
		--i;
		System.out.println(i);
		
		int j = 10;
		
		System.out.println(j++);
		System.out.println(j);
		System.out.println(++j);
		System.out.println(j);
	}
}
