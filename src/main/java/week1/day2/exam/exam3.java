package week1.day2.exam;

public class exam3 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 4;
		int ans = 0;
		
		ans = a + b;
		System.out.println(a + "+" + b + "= " + ans);
		
		ans = a - b;
		System.out.println(a + "-" + b + "= " + ans);
		
		ans = a * b;
		System.out.println(a + "*" + b + "= " + ans);
		
		float ansFloat = (float)a / (float)b;
		System.out.println(a + "+" + b + "= " + ansFloat);
	}
}
