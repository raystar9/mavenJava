package week1.day4;

public class OddEven {

	public static void main(String[] args) {
		
		int i = 0;
		
		i = 1;
		while(i < 10) {
			System.out.println(i);
			i += 2;
		}
		System.out.println("even number print ends");
		
		i = 2;
		while(i < 10) {
			System.out.println(i);
			i += 2;
		}
		System.out.println("odd number print ends");
	}
}
